"""Исключения для booking-модуля.

Этот модуль является единой точкой для предметных ошибок бронирования.
Это важно для бизнеса, потому что:
    - API и web-flow должны одинаково трактовать причины отказа в бронировании;
    - ошибки должны быть понятными для пользователя и предсказуемыми для кода;
    - booking-логика не должна разбрасывать текстовые исключения по случайным файлам.
"""


class BookingError(Exception):
    """Базовая предметная ошибка booking-модуля.

    От этого класса наследуются все ожидаемые ошибки бронирования,
    которые не являются системными падениями, а означают контролируемый
    отказ выполнить бизнес-операцию.
    """


class CreateTherapySessionValidationError(BookingError):
    """Ошибка валидации сценария CreateTherapySession.

    Используется, когда:
        - клиент передал некорректные входные данные;
        - выбранный слот уже недоступен;
        - у специалиста нет рабочего правила;
        - создание встречи противоречит текущим бизнес-инвариантам.
    """


class ParseSlotValidationError(BookingError):
    """Ошибка валидации преобразования ISO-строки в слотах в aware datetime."""
