from rest_framework.throttling import UserRateThrottle


class CreateTherapySessionThrottle(UserRateThrottle):
    """Throttle-класс для защиты API-сценария создания терапевтической сессии.

    Этот throttle работает по идентификатору авторизованного пользователя, а не по IP,
    потому что текущий endpoint доступен только после аутентификации клиента.

    Для чего нужен CreateTherapySessionThrottle:
        - ограничивает слишком частые повторные попытки создать встречу;
        - снижает риск спама запросами на бронирование;
        - уменьшает нагрузку на backend при многократных кликах по кнопке создания сессии;
        - дополнительно страхует систему от случайного или злонамеренного создания большого числа событий.
    """

    scope = "therapy_session_create"
