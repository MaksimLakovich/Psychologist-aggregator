def get_event_active_slot(event):
    """Возвращает актуальный слот события из уже загруженного набора слотов.

    Бизнес-смысл:
        - и страница "Мой календарь", и detail-страница конкретной встречи должны одинаково понимать,
          какой слот из события считать текущим рабочим слотом;
        - пока у нас нет отдельного полноценного multi-slot сценария для event, в интерфейсе нужен один актуальный
          слот, с которым дальше работают карточка и detail-страница;
        - helper нужен как единый источник истины, чтобы правило выбора слота не дублировалось в нескольких view.
    """
    # Для краткой карточки в списке нужно брать не "самый первый слот вообще", а первый АКТУАЛЬНЫЙ слот:
    #   - либо еще запланированный;
    #   - либо уже начавшийся.
    # Это важно для будущих multi-slot событий.
    #
    # Пример:
    #   - у события "Курс" три урока;
    #   - первый урок уже completed;
    #   - второй урок planned;
    #   - третий урок planned.
    # В таком случае на странице "Мой календарь" нужно показывать не первый завершенный урок,
    # а ближайший еще актуальный урок курса.
    #
    # Важно: фильтруем уже загруженный через prefetch набор слотов, чтобы не делать доп SQL-запрос на каждое событие
    active_slots = [
        slot
        for slot in event.slots.all()
        if slot.status in ["planned", "started"]
    ]

    # TODO: Для будущих multi-slot событий нужно изменить эту строку и логику страницы в целом,
    #  так как сейчас "эта страница = список событий", а если нам нужно показывать multi-slot события,
    #  то, наверное, логично показывать каждый будущий урок (slot) отдельно, а не общее event для всех уроков
    return next(iter(active_slots), None)


def get_event_completed_slot(event):
    """Возвращает последний завершенный слот события из уже загруженного набора слотов.

    Бизнес-смысл:
        - для прошедших событий на странице "Мой календарь" клиенту важно видеть не будущий или случайный слот,
          а именно тот слот, который уже завершился или относится к архиву (например, дата в прошлом);
        - пока полноценного multi-slot сценария у нас нет, для архивной карточки тоже нужен один
          репрезентативный слот, который объясняет пользователю, когда именно эта сессия прошла;
        - helper вынесен отдельно, чтобы логика выбора архивного слота не расползалась по view и логично делать
          похожую функцию в одном архитектурном стиле как ранее сделали в get_event_active_slot().
    """
    # Для архивной карточки берем не "первый слот вообще", а последний уже завершенный слот:
    #   - сначала приоритет у статуса completed;
    #   - если статусная модель по какой-то причине еще не обновилась,
    #     fallback-логика может опираться на уже завершившееся по времени окно во view.
    completed_slots = [
        slot
        for slot in event.slots.all()
        if slot.status == "completed"
    ]

    # Так как slots уже отсортированы по start_datetime в prefetch queryset,
    # разворачиваем список и берем самый поздний завершенный слот:
    # именно он лучше всего объясняет клиенту, когда событие в последний раз реально прошло.
    # TODO: Для будущих multi-slot событий нужно изменить эту строку и логику страницы в целом
    if completed_slots:
        return next(iter(reversed(completed_slots)), None)

    return None
