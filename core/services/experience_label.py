# Сервис для формирования человеко-понятной подписи опыта психолога с КОРРЕКТНЫМ окончанием.
#     Использование:
#         - build_experience_label(None) -> "Опыт не указан"
#         - build_experience_label(1) -> "Опыт 1 год"
#         - build_experience_label(4) -> "Опыт 4 года"
#         - build_experience_label(12) -> "Опыт 12 лет"


def _pluralize_year_word(years: int) -> str:
    """Возвращает корректную форму слова "год" для числа years.
        Правила:
            - 1, 21, 31 - "год" (пример, "Опыт 21 год").
            - 2, 3, 4 (кроме 12-14) - "года" (пример, "Опыт 4 года").
            - все остальные - "лет" (пример, "Опыт 25 лет").
    """
    remainder_100 = years % 100  # Остаток от деления на 100 (последние две цифры)
    remainder_10 = years % 10  # Остаток от деления на 10 (последняя цифра)

    # Числа, заканчивающиеся на 11–14 "remainder_100", всегда требуют слова "лет" (исключение 1)
    if 11 <= remainder_100 <= 14:
        return "лет"
    # Числа, заканчивающиеся на 1 (и это не 11), всегда пишем "год" (пример: 1 год, 21 год, 101 год)
    if remainder_10 == 1:
        return "год"
    # Числа, заканчивающиеся на 2-4 (и это не 12, 13, 14), всегда пишем "года" (пример: 2 года, 34 года)
    if 2 <= remainder_10 <= 4:
        return "года"
    # Для всех остальных цифр (5, 6, 7, 8, 9, 0) используется "лет" (пример: 5 лет, 20 лет, 100 лет)
    return "лет"


def build_experience_label(work_experience_years) -> str:
    """Формирует итоговую строку для UI в формате "Опыт N лет".
        Что делает:
            1) Если опыт не указан - "Опыт не указан".
            2) Иначе приводит значение к int.
            3) Подбирает корректное окончание для слова "год".
        Важно:
            - это единая точка правды для всего backend-кода;
            - одинаково используется в каталоге и в API шага выбора психолога.
    """
    if work_experience_years is None:
        return "Опыт не указан"

    try:
        years = int(work_experience_years)
    except (TypeError, ValueError):
        return "Опыт не указан"

    if years < 0:
        return "Опыт не указан"

    suffix = _pluralize_year_word(years)

    return f"Опыт {years} {suffix}"
